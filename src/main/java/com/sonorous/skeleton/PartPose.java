package com.sonorous.skeleton;


public class PartPose {
    private float x, y, z;

    public PartPose(float x, float y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}